 function  [wls, bp] = c1_postprocessing(wls, bp, user)
  
 

%--------------------------Spectral Radius---------------------------------
 if user.radius == 1
    [bp] = c1_spectral_rad(user, bp);
 end
%-------------------------------------------------------------------------- 


%-------------------State Estimation Evaluation----------------------------
 if user.radius == 1
    [wls, bp] = c2_evaluation(wls, bp);
 end    
%--------------------------------------------------------------------------


%-------------------------------Display------------------------------------
 d1_disp(bp, wls, user);
%--------------------------------------------------------------------------